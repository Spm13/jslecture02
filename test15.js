//Arrow Function

const aa = () => {
        console.log(111)
}
const aaa = () => console.log(111)

const bb = () => {
        return 222 * 10
}
const bbb = () => 222 * 10

const cc = (xx) => {
        console.log(xx)
}
const ccc = xx => console.log(xx)

const dd = (yy) => {
        return yy * 10
}
const ddd = yy => yy * 10